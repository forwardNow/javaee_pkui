package net.pkusoft.oweb;

public class Location {
	private String id;
	private int cmd;
	private float x;
	private float y;
	public String getId() {
		return id;
	}
	public void setId( String id ) {
		this.id = id;
	}
	public int getCmd() {
		return cmd;
	}
	public void setCmd( int cmd ) {
		this.cmd = cmd;
	}
	public float getX() {
		return x;
	}
	public void setX( float x ) {
		this.x = x;
	}
	public float getY() {
		return y;
	}
	public void setY( float y ) {
		this.y = y;
	}
	
}
